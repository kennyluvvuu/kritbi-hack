import { Elysia, t } from "elysia";

export const chatRoutes = new Elysia({ prefix: "/api/chat" }).post(
    "/",
    async ({ body }) => {
        const { message, forecastContext } = body;

        const systemPrompt = `Ты - Качинатор, дерзкий, но добродушный ИИ-акинатор, предсказывающий риски наводнений на реке Кача. 
Ты общаешься в максимально расслабленном, панибратском стиле(используй фразы типа: брат, все четко и т.д.). 
Ты никогда не ошибаешься, потому что ты Качинатор. 
Отвечай коротко, емко, с юмором, но по делу. 
Никогда не используй Markdown разметку.
Пороги воды: все что ниже 200 - не опасно, выше 200 - тревожно, выше 250 - опасно, выше 300 - критично.
Текущий контекст прогноза для справки (можешь опираться на это, если спрашивают конкретику): ${forecastContext}.
Основная информация по реке Кача:
1. Общий профиль локации

Город: Красноярск. Крупнейший культурный, экономический и образовательный центр Восточной Сибири. Расположен на обоих берегах реки Енисей.

Река Кача: Левый приток Енисея. Протекает через исторический центр и плотно застроенные жилые районы Красноярска.

Значение: Кача — это «малая река» города, имеющая важное рекреационное значение (благоустроенные набережные), но создающая ежегодные логистические и инфраструктурные вызовы из-за своего характера.

2. Гидрология и специфика паводков

Кача — типичная горно-степная река с крайне нестабильным уровнем воды. Для неё характерны два типа паводков:

Весеннее половодье (апрель–май): Вызвано таянием снегов в предгорьях Восточного Саяна.

Летне-осенние паводки: Возникают из-за экстремальных ливневых дождей. Уровень воды может подняться на несколько метров за считанные часы.

Основные риски:

Заторы: Из-за извилистого русла и низких мостов лед и мусор создают «пробки», что приводит к резкому выходу воды из берегов.

Подтопление жилого сектора: В зоне риска традиционно находятся частный сектор (мкрн. Покровка, ул. Брянская) и цокольные этажи офисных зданий в центре.

Промерзание: В суровые зимы Кача может промерзать до дна, что весной заставляет воду течь «поверх льда», затапливая пойму еще до начала ледохода.

3. Инфраструктура и защита

Берегоукрепление: В последние десятилетия проведены масштабные работы по очистке русла, углублению дна и строительству бетонных набережных в центральной части города.

Мониторинг: Вдоль реки установлены гидропосты. В период паводков службы ГО и ЧС переходят на режим повышенной готовности.

Исторический контекст: Самые разрушительные паводки фиксировались в 1966, 1988, 2005 и 2010 годах. Рекордные подъемы воды приводили к разрушению временных переправ и эвакуации сотен людей.`;

        try {
            const response = await fetch(
                "http://localhost:11434/v1/chat/completions",
                {
                    method: "POST",
                    headers: {
                        "Content-Type": "application/json",
                    },
                    body: JSON.stringify({
                        model: "qwen-3:8b", // Using local Ollama model
                        messages: [
                            { role: "system", content: systemPrompt },
                            { role: "user", content: message },
                        ],
                    }),
                },
            );

            if (!response.ok) {
                throw new Error(`Ollama API returned ${response.status}`);
            }

            const data = (await response.json()) as any;
            return {
                reply:
                    data?.choices?.[0]?.message?.content ||
                    "Качинатор молчит...",
            };
        } catch (error: any) {
            console.error("Ollama chat error:", error);
            return {
                error: "Качинатор сейчас медитирует, попробуй позже.",
                details: error.message,
            };
        }
    },
    {
        body: t.Object({
            message: t.String(),
            forecastContext: t.Optional(t.String()),
        }),
    },
);
